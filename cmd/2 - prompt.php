<!-- Comandos Prompt -->

DIR - Mostra os arquivos do diretório atual
COPY - Copia os arquivos entre diretórios ou unidades de disco diferentes
DEL - Apaga um arquivo
DELTREE - Apaga um diretório e todos os subdiretórios e arquivos
CLS - Limpa a tela
FORMAT - Formata uma unidade de disco
MD - Cria um diretorio
RD - Remove um diretório vazio
CD - Muda do diretório atual para outro
CD.. - Volta para o diretório anterior
CD\ - Volta para o diretório raiz
VER - Mostra a versão do SO
MEM - Mostra a quantidade de memoria disponível
TREE - Mostra a estrutura de diretórios
LOCATE - Localiza um arquivo no disco
TYPE - Mostra o conteúdo de um arquivo ASC II
TIME - Mostra e ou ajusta a hora atual
DATE - Mostra e ou ajusta a data atual
SYS - Transfere o sistema para outro disco
HELP - Ajuda (também funciona digitando o comando e /? Ex: DIR /?)